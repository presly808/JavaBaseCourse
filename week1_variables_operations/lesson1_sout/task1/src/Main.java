// class declaration, the first block in the program.
// It has its boundaries presented as brackets {}
public class Main {
   // main method declaration, block in the program where our instructions to a machine are written
   // Each block has brackets {}
   // String[] args - arguments (data)
   // one of the ways how data can be passed to the program from external.
   public static void main(String[] args) {
      // System - other class which know how to work with a system environment
      // out - component of `System` which has access to a console, where we can print something
      // println - method (function) of `out` -> that accepts data ("Hello World!") and prints to console
      // "Hello World!" -> String value wrapped with "
      System.out.println("Hello World!");
   }
}